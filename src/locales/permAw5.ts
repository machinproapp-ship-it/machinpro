function flatFromRole(
  groups: Record<string, string>,
  body: Record<keyof RolePermissions, string>
): Record<string, string> {
  const out: Record<string, string> = { ...groups };
  for (const k of ROLE_PERMISSION_KEYS) {
    out[permLocaleKey(k)] = body[k];
  }
  return out;
}

